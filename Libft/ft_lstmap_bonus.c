#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*list;
	t_list	*new;
	void	*value;

	list = NULL;
	while (lst && f && del)
	{
		value = f(lst->content);
		new = ft_lstnew(value);
		if (!new)
		{
			del(value);
			ft_lstclear(&list, del);
			return (NULL);
		}
		ft_lstadd_back(&list, new);
		lst = lst->next;
	}
	return (list);
}
