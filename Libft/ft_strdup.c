#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	len;
	char	*dest;
	size_t	i;

	len = ft_strlen(src);
	i = 0;
	dest = malloc(sizeof(char) * (len + 1));
	if (!dest)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
